package iseg.app.model;

public class OrcamentoSeguradoraCoberturaServico {
	
	private String seguradora;
	private String coberturaOuServico;
	private Double premio;
	
	public String getSeguradora() {
		return seguradora;
	}
	public void setSeguradora(String seguradora) {
		this.seguradora = seguradora;
	}
	public String getCoberturaOuServico() {
		return coberturaOuServico;
	}
	public void setCoberturaOuServico(String coberturaOuServico) {
		this.coberturaOuServico = coberturaOuServico;
	}
	public Double getPremio() {
		return premio;
	}
	public void setPremio(Double premio) {
		this.premio = premio;
	}
	
}
